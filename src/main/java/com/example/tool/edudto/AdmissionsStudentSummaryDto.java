package com.example.tool.edudto;

public class AdmissionsStudentSummaryDto {
	private Long totalNumber;
	private Long numberAdmissions;
	private Long numberAdmissionsByUser;
	public Long getTotalNumber() {
		return totalNumber;
	}
	public void setTotalNumber(Long totalNumber) {
		this.totalNumber = totalNumber;
	}
	public Long getNumberAdmissions() {
		return numberAdmissions;
	}
	public void setNumberAdmissions(Long numberAdmissions) {
		this.numberAdmissions = numberAdmissions;
	}
	public Long getNumberAdmissionsByUser() {
		return numberAdmissionsByUser;
	}
	public void setNumberAdmissionsByUser(Long numberAdmissionsByUser) {
		this.numberAdmissionsByUser = numberAdmissionsByUser;
	}
	
	
}
